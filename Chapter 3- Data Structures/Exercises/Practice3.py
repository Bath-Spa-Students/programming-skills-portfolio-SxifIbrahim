# Lens Function
Number = [2,5,6,2,1,8,8,1,6]
print("number of elements in a list :" ,len(Number))

# Mutability (Changable)
Numbers = [5,1,4,2,6,8]
Numbers[0] = 9
Numbers[1] = 3
print(Numbers)

# Concatenation
List_1 = [1,3,4,6,1,8]
List_2 = [5,2,8,1,8,1]
List_3 = List_1 + List_2
print(List_3)

# List Slicing
new_list = [2,3,1,5,7,8]
result = new_list [1:4] # 1st Index Inclusive, 2nd Index Exclusive
print(result)