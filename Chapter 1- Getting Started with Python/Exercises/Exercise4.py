""" 
Write three strings in different variables and print the output as one string. 
"""

# Define 3 strings in different Variable
Lastname = "Ibrahim"
Firstname = "Seif"
Agenumber = "18 years old"

# Concentrate the Strings into one and Print the Combined String
print ("Hi! My name is" + " " + Firstname + " " + Lastname + ", i am " + Agenumber)